public class shifts {
    public static void main(String args[]){
        // left shift << 
        // In this case we shift 1 in binary towords left
        // formula for getting result in decimal is  a* (2's power b) where a<<b;
        System.out.println(5<<1);

        // Right shift >>
        // In this case we shift 1 in binary number towords right
        // formula for getting result in decimal is a / (2's power b) where a>>b;
        System.out.println(10>>1);
    }
}
